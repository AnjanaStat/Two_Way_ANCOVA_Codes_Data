LRT_a3_b2<-function(n11,n12,n21,n22,n31,n32,a1,a2,a3,b1,b2,p,v11,v12,v21,v22,v31,v32){
  cl<-makeCluster(4)
  registerDoParallel(cl)
  S<-1000
  #start_time <- Sys.time()
  count_vec<-foreach(s=1:S, .combine=rbind)%dopar%{
    set.seed(s)
    ##fun1 finds the observed value of LRT, estimators of $beta_j$'s, and variances under the null parameter space
  fun1<-function(n11,n12,n21,n22,n31,n32,a1,a2,a3,b1,b2,p,v11,v12,v21,v22,v31,v32)
  {
    X11=rnorm(n11,2,1);X12=rnorm(n12,3,1)
    X21=rnorm(n21,2,1);X22=rnorm(n22,3,1)
    X31=rnorm(n31,2,1);X32=rnorm(n32,3,1)
    mu11=a1+b1+p*X11;mu12=a1+b2+p*X12
    mu21=a2+b1+p*X21;mu22=a2+b2+p*X22
    mu31=a3+b1+p*X31;mu32=a3+b2+p*X32
    Y11=rnorm(n11,mu11,sqrt(v11));Y12=rnorm(n12,mu12,sqrt(v12))
    Y21=rnorm(n21,mu21,sqrt(v21));Y22=rnorm(n22,mu22,sqrt(v22))
    Y31=rnorm(n31,mu31,sqrt(v31));Y32=rnorm(n32,mu32,sqrt(v32))
    y11=mean(Y11);y12=mean(Y12);y21=mean(Y21);y22=mean(Y22)
    y31=mean(Y31);y32=mean(Y32)
    y=(y11+y12+y21+y22+y31+y32)/6
    y.1=(y11+y21+y31)/3;y.2=(y12+y22+y32)/3
    y1.=(y11+y12)/2;y2.=(y21+y22)/2;y3.=(y31+y32)/2
    x11=mean(X11);x12=mean(X12);x21=mean(X21);x22=mean(X22)
    x31=mean(X31);x32=mean(X32)
    x=(x11+x12+x21+x22+x31+x32)/6
    x.1=(x11+x21+x31)/3;x.2=(x12+x22+x32)/3
    x1.=(x11+x12)/2;x2.=(x21+x22)/2;x3.=(x31+x32)/2
    n.1=(n11+n21+n31);n.2=(n12+n22+n32)
    yw.1=(n11*y11+n21*y21+n31*y31)/n.1;yw.2=(n12*y12+n22*y22+n32*y32)/n.2
    xw.1=(n11*x11+n21*x21+n31*x31)/n.1;xw.2=(n12*x12+n22*x22+n32*x32)/n.2
    nu0n=sum((Y11-yw.1)*(X11-xw.1))+sum((Y12-yw.2)*(X12-xw.2))+
      sum((Y21-yw.1)*(X21-xw.1))+sum((Y22-yw.2)*(X22-xw.2))+
      sum((Y31-yw.1)*(X31-xw.1))+sum((Y32-yw.2)*(X32-xw.2))
    nu0d=sum((X11-xw.1)^2)+sum((X12-xw.2)^2)+sum((X21-xw.1)^2)+sum((X22-xw.2)^2)+
      sum((X31-xw.1)^2)+sum((X32-xw.2)^2)
    nu0=nu0n/nu0d
    b01=yw.1-nu0*xw.1;b02=yw.2-nu0*xw.2
    b0=c(b01,b02)
    s11=(sum((Y11-y11)^2)-(sum((Y11-y11)*(X11-x11)))^2/sum((X11-x11)^2))/(n11-2)
    s12=(sum((Y12-y12)^2)-(sum((Y12-y12)*(X12-x12)))^2/sum((X12-x12)^2))/(n12-2)
    s21=(sum((Y21-y21)^2)-(sum((Y21-y21)*(X21-x21)))^2/sum((X21-x21)^2))/(n21-2)
    s22=(sum((Y22-y22)^2)-(sum((Y22-y22)*(X22-x22)))^2/sum((X22-x22)^2))/(n22-2)
    s31=(sum((Y31-y31)^2)-(sum((Y31-y31)*(X31-x31)))^2/sum((X31-x31)^2))/(n31-2)
    s32=(sum((Y32-y32)^2)-(sum((Y32-y32)*(X32-x32)))^2/sum((X32-x32)^2))/(n32-2)
    s011=s11;s012=s12;s021=s21;s022=s22;s031=s31;s032=s32
    nu=nu0
    repeat
    {
      u11=n11/s011;u12=n12/s012;u21=n21/s021;u22=n22/s022;u31=n31/s031;u32=n32/s032
      u.1=(u11+u21+u31);u.2=(u12+u22+u32)
      y.n1=(u11*y11+u21*y21+u31*y31)/u.1;y.n2=(u12*y12+u22*y22+u32*y32)/u.2
      x.n1=(u11*x11+u21*x21+u31*x31)/u.1;x.n2=(u12*x12+u22*x22+u32*x32)/u.2
      bn1=y.n1-nu*x.n1;bn2=y.n2-nu*x.n2
      bn=c(bn1,bn2)
      nunn=sum((Y11-bn1)*X11)/s011+sum((Y12-bn2)*X12)/s012+
        sum((Y21-bn1)*X21)/s021+sum((Y22-bn2)*X22)/s022+
        sum((Y31-bn1)*X31)/s031+sum((Y32-bn2)*X32)/s032
      nund=sum(X11^2)/s011+sum(X12^2)/s012+sum(X21^2)/s021+
          sum(X22^2)/s022+sum(X31^2)/s031+sum(X32^2)/s032
      nun=nunn/nund
      sn11=sum((Y11-bn1-nun*X11)^2)/n11;sn12=sum((Y12-bn2-nun*X12)^2)/n12
      sn21=sum((Y21-bn1-nun*X21)^2)/n21;sn22=sum((Y22-bn2-nun*X22)^2)/n22
      sn31=sum((Y31-bn1-nun*X31)^2)/n31;sn32=sum((Y32-bn2-nun*X32)^2)/n32
      d1=abs(nu-nun);d2=max(abs(bn-b0))
      if(d1<=0.00001 & d2<=0.00001)
      {
        break
      }
      nu=nun;b0=bn
      s011=sn11;s012=sn12;s021=sn21;s022=sn22;s031=sn31;s032=sn32
      }
      Nu0n=sum((Y11-y.1-y1.+y)*(X11-x.1-x1.+x))+sum((Y12-y1.-y.2+y)*(X12-x1.-x.2+x))+
        sum((Y21-y2.-y.1+y)*(X21-x2.-x.1+x))+sum((Y22-y2.-y.2+y)*(X22-x2.-x.2+x))+
        sum((Y31-y3.-y.1+y)*(X31-x3.-x.1+x))+sum((Y32-y3.-y.2+y)*(X32-x3.-x.2+x))
      Nu0d=sum((X11-x1.-x.1+x)^2)+sum((X12-x1.-x.2+x)^2)+sum((X21-x2.-x.1+x)^2)+
        sum((X22-x2.-x.2+x)^2)+sum((X31-x3.-x.1+x)^2)+sum((X32-x3.-x.2+x)^2)
      Nu0=Nu0n/Nu0d;Nu=Nu0
      A01=y1.-Nu0*x1.;A02=y2.-Nu0*x2.;A03=y3.-Nu0*x3.
      A0=c(A01,A02,A03)
      #M=y-Nu0*x
      B01=y.1-y-Nu0*(x.1-x);B02=y.2-y-Nu0*(x.2-x)
      B0=c(B01,B02)
      S011=s11;S012=s12;S021=s21;S022=s22;S031=s31;S032=s32
      repeat
      {
        U11=n11/S011;U12=n12/S012;U21=n21/S021;U22=n22/S022;U31=n31/S031;U32=n32/S032
        W1=(U11+U12);W2=(U21+U22);W3=(U31+U32)
        W=c(W1,W2,W3)
        Yn1=(U11*(y11-B0[1]-Nu*x11)+U12*(y12-B0[2]-Nu*x12))/W1
        Yn2=(U21*(y21-B0[1]-Nu*x21)+U22*(y22-B0[2]-Nu*x22))/W2
        Yn3=(U31*(y31-B0[1]-Nu*x31)+U32*(y32-B0[2]-Nu*x32))/W3
        Yn=c(Yn1,Yn2,Yn3)
        An=Iso::pava(Yn,W)
        V1=U11*(y11-An[1]-Nu*x11)+U21*(y21-An[2]-Nu*x21)+U31*(y31-An[3]-Nu*x31)-
          (U12*(y12-An[1]-Nu*x12)+U22*(y22-An[2]-Nu*x22)+U32*(y32-An[3]-Nu*x32))
        Bn1=V1/(U11+U21+U31+U12+U22+U32);Bn2=-Bn1
        Bn=c(Bn1,Bn2)
        Nunn=sum((Y11-An[1]-Bn[1])*X11)/S011+sum((Y12-An[1]-Bn[2])*X12)/S012+
          sum((Y21-An[2]-Bn[1])*X21)/S021+sum((Y22-An[2]-Bn[2])*X22)/S022+
          sum((Y31-An[3]-Bn[1])*X31)/S031+sum((Y32-An[3]-Bn[2])*X32)/S032
        Nund=sum(X11^2)/S011+sum(X12^2)/S012+sum(X21^2)/S021+sum(X22^2)/S022+
          sum(X31^2)/S031+sum(X32^2)/S032
        Nun=Nunn/Nund
        Sn11=sum((Y11-An[1]-Bn[1]-Nun*X11)^2)/n11
        Sn12=sum((Y12-An[1]-Bn[2]-Nun*X12)^2)/n12
        Sn21=sum((Y21-An[2]-Bn[1]-Nun*X21)^2)/n21
        Sn22=sum((Y22-An[2]-Bn[2]-Nun*X22)^2)/n22
        Sn31=sum((Y31-An[3]-Bn[1]-Nun*X31)^2)/n31
        Sn32=sum((Y32-An[3]-Bn[2]-Nun*X32)^2)/n32
        #c(Sn11,Sn12,Sn21,Sn22,Sn31,Sn32)
        D1=max(abs(A0-An));D2=max(abs(B0-Bn));D3=abs(Nu-Nun)
        if(D1<=0.00001 & D2<=0.00001 & D3<=0.00001)
        {
          break
        }
        A0=An;B0=Bn;Nu=Nun
        S011=Sn11;S012=Sn12;S021=Sn21;S022=Sn22;S031=Sn31;S032=Sn32
      }
      LR=(Sn11/sn11)^(n11/2)*(Sn12/sn12)^(n12/2)*(Sn21/sn21)^(n21/2)*
        (Sn22/sn22)^(n22/2)*(Sn31/sn31)^(n31/2)*(Sn32/sn32)^(n32/2)
      value=c(LR,b01,b02,nu0,s11,s12,s21,s22,s31,s32)
      return(value)
  }
  ##fun2 finds critical point
    fun2<-function(n11,n12,n21,n22,n31,n32,a1,a2,a3,b1,b2,p,v11,v12,v21,v22,v31,v32)
    {
      B<-1000
      out<-fun1(n11,n12,n21,n22,n31,n32,a1,a2,a3,b1,b2,p,v11,v12,v21,v22,v31,v32)
      LR=out[1]
      b01=out[2];b02=out[3];nu0=out[4];s11=out[5];s12=out[6];s21=out[7];s22=out[8]
      s31=out[9];s32=out[10]
      X<-rep(NA,B)
      for(b in 1:B)
      {
        out<-fun1(n11,n12,n21,n22,n31,n32,0,0,0,b01,b02,nu0,s11,s12,s21,s22,s31,s32)
        X[b]<-out[1]
      }
      y<-sort(X,decreasing=FALSE)
      crit<-y[50]
      rslt=c(LR,crit)
      return(rslt)
    }
    ##fun3 finds size/power value of LRT
    fun3<-function(n11,n12,n21,n22,n31,n32,a1,a2,a3,b1,b2,p,v11,v12,v21,v22,v31,v32)
    {
      output<-fun2(n11,n12,n21,n22,n31,n32,a1,a2,a3,b1,b2,p,v11,v12,v21,v22,v31,v32)
      LR=output[1];crit=output[2]
      count=0
      if(LR<crit)
      {
        count=count+1
      }
      return(count)
    }
    r<-fun3(n11,n12,n21,n22,n31,n32,a1,a2,a3,b1,b2,p,v11,v12,v21,v22,v31,v32)
    return(r)
  }
  #end_time <- Sys.time()
  stopCluster(cl)
  #cat("Parallel execution time:",end_time - start_time, "\n")
  size=mean(count_vec)
  return(size)
}

